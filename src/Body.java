public class Body {

    private String eyes;
    private String height;
    public Body(String eyes, String height) {
        this.eyes = eyes;
        this.height = height;
    }
    public void setEyes(String eyes) {
        this.eyes = eyes;
    }
    public void setHeight(String height) {
        this.height = height;
    }

    public String getEyes() {
        return eyes;
    }
    public String getHeight() {
        return height;
    }
}
